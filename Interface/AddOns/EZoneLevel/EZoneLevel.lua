--[[
	This is Ezarras ZoneLevel, based on Zone Level by Fanook
	Additions to this version include 1) More zones, 2) a tooltip window showing 
	the low and high levels for a zone when you mouse over the ZoneLevel icon, and
	a Message to the chat window when changing zones showing the low and high 
	levels.  Zome zones have the levels slightly tweaked to give more realistic
	information on the actual level of a zone 
	
	The Zones are all alphabetized for easy searching and reference.
-- ]]

local EZLL; --// the low data
local EZLH; --// the high data
local ZL_low; --// the lowest level
local ZL_high; --// The highest level
local level; --// The player level
local zoneColor   = ""; --// The Color commands for the zone
local zone        = ""; --// The SubZone or RealZone
local LastChatSpam= "";
local EZLGrey     = "|cff999999";
local EZLRed      = "|cffff0000";
local EZLGreen    = "|cff00ff00";
local EZLYellow   = "|cffffff00";
local EZLWhite    = "|cffffffff";
local text        = "";
-- //Set Default 

function EZoneLevel_Command(msg)
    -- // We don't care about caps
    msg = string.lower(msg);
    
    if ( (string.find(msg, 'on'))) then
        ChatSpam = 1;
        ChatFrame1:AddMessage(EZLYellow.."EZoneLevel ChatSpam|r"..EZLRed.." Enabled");
    elseif ( (string.find(msg, 'off'))) then
        ChatSpam = 0;
        ChatFrame1:AddMessage(EZLYellow.."EZoneLevel ChatSpam|r"..EZLRed.." Disabled");
    elseif ( (string.find(msg, 'unk'))) then
        ChatSpam = 2;
        ChatFrame1:AddMessage(EZLYellow.."EZoneLevel ChatSpam|r"..EZLRed.." Unknown");
    elseif ( (string.find(msg, 'sta'))) then
        if (ChatSpam == 1) then
            ChatFrame1:AddMessage(EZLYellow.."EZoneLevel ChatSpam|r"..EZLRed.." Enabled");
        elseif (ChatSpam == 0) then
            ChatFrame1:AddMessage(EZLYellow.."EZoneLevel ChatSpam|r"..EZLRed.." Disabled");
        elseif (ChatSpam == 2) then
            ChatFrame1:AddMessage(EZLYellow.."EZoneLevel ChatSpam|r"..EZLRed.." Unknown");
        end
    elseif ( (string.find(msg, 'tog'))) then
        if (ChatSpam == 1) then
            ChatSpam = 0;
            ChatFrame1:AddMessage(EZLYellow.."EZoneLevel ChatSpam|r"..EZLRed.." Disabled");
        else
            ChatSpam = 1;
            ChatFrame1:AddMessage(EZLYellow.."EZoneLevel ChatSpam|r"..EZLRed.." Enabled");
        end
    else
         ChatFrame1:AddMessage(EZLRed.."/ezl on|r "..EZLYellow.."turns on ChatSpam");
         ChatFrame1:AddMessage(EZLRed.."/ezl off|r "..EZLYellow.."turns off ChatSpam");
         ChatFrame1:AddMessage(EZLRed.."/ezl unknown|r "..EZLYellow.."turns ChatSpam on only for unknown areas");
         ChatFrame1:AddMessage(EZLRed.."/ezl status|r "..EZLYellow.."shows current state");
         ChatFrame1:AddMessage(EZLRed.."/ezl toggle|r "..EZLYellow.."Toggles ChatSpam ");
         ChatFrame1:AddMessage(EZLRed.."ON|r or "..EZLRed.."OFF|r "..EZLYellow.."(toggles "..EZLRed.."UNKNOWN|r "..EZLYellow.."to ON)");
    end
    RegisterForSave("ChatSpam");
end

function EZoneLevel_OnEnter()
    GameTooltip:SetOwner(EZoneLevel_Base, "ANCHOR_NONE");
    GameTooltip:SetPoint("TOP", "UIParent", "TOPRIGHT", -265, -20);
    EZoneLevel_SetTooltip();
end

-- //Load/initialization function
function EZoneLevel_OnLoad()

    -- //Register for events
    this:RegisterEvent("MINIMAP_ZONE_CHANGED");
    
    if( DEFAULT_CHAT_FRAME ) then
        DEFAULT_CHAT_FRAME:AddMessage(EZLRed.."Ezarra's|r ZoneLevel Loaded\n"..EZLYellow.."/ezl help|r for help");
    end
    
    UIErrorsFrame:AddMessage("EZone Level AddOn loaded", 1.0, 1.0, 1.0, 1.0, UIERRORS_HOLD_TIME);
    
    --//Register Slash Commands
    SlashCmdList["EZONE"] = EZoneLevel_Command;
    SLASH_EZONE1 = "/ezl";
    SLASH_EZONE2 = "/ezonelevel";
     if(myAddOnsFrame) then
     	myAddOnsList.EZoneLevel = {name = "EZoneLevel", description = "Adds a relative safety graphical indicator of mob levels in a zone; /ezl for help", version = "1.8", category = MYADDONS_CATEGORY_MAP, frame = "EZoneLevel_Base"};
	end
    this:RegisterEvent("VARIABLES_LOADED");
       
    -- // Construct tables to hold level information for each zone
    EZLL={};
    EZLH={};
    EZoneLevel_MakeTables();
    
    -- // Rest Previous message
    LastChatSpam = "";
    
end

-- //Event handler for ZoneLevel
function EZoneLevel_OnEvent()
    level = UnitLevel("player");
    zone=GetSubZoneText();
    if ((zone == "") or (EZLL[zone] == -6)) then 
       zone=GetZoneText();
    end
    ZL_low = EZLL[zone];
    text = "";
    unknown = 0;
    if ( ZL_low == nil ) then
        if (zone ~= "") then
            text = EZLRed.."Levels for Zone \""..zone.."\" unknown|r, ";
            unknown = 1;
        end
        zone=GetZoneText();
        ZL_low = EZLL[zone];
        ZL_high = EZLH[zone];
        text = text .."using "..zone.." levels.\n";
        EZoneLevel_Grey:Show();
        EZoneLevel_Green:Hide();
        EZoneLevel_Yellow:Hide();
        EZoneLevel_Red:Hide();
        if (ChatSpam == 2 )  then
            if (text ~= LastChatSpam) then
                   ChatFrame1:AddMessage(text);
                   LastChatSpam = text;
            end
        end
    else
        ZL_high = EZLH[zone];
        local mid = (ZL_low + ZL_high)/2;
        if(ZL_high < level-5) then -- Grey icon
            EZoneLevel_Grey:Show();
            EZoneLevel_Green:Hide();
            EZoneLevel_Yellow:Hide();
            EZoneLevel_Red:Hide();
            zoneColor = EZLGrey;
        elseif(ZL_low > level) then -- Red icon
            EZoneLevel_Grey:Hide();
            EZoneLevel_Green:Hide();
            EZoneLevel_Yellow:Hide();
            EZoneLevel_Red:Show();
            zoneColor = EZLRed;
        elseif(mid < level) then -- Green icon
            EZoneLevel_Grey:Hide();
            EZoneLevel_Green:Show();
            EZoneLevel_Yellow:Hide();
            EZoneLevel_Red:Hide();
            zoneColor = EZLGreen;
        else -- Yellow icon
            EZoneLevel_Grey:Hide();
            EZoneLevel_Green:Hide();
            EZoneLevel_Yellow:Show();
            EZoneLevel_Red:Hide();
            zoneColor = EZLYellow;
        end
    end
    if (zoneColor == "") then
        zoneColor = EZLWhite;
    end
    if (ChatSpam ~= 0) then
        text = zone.."|r ";
    else
        text = zone.." ";
    end
    if (ZL_low ~= nil) then
        text = text.."("..ZL_low;
    end
    if (ZL_high ~= nil) then
        text = text.."/"..ZL_high..")";
    end
    if ( ChatSpam == 2 ) then
        if (unknown == 1 )  then
             if (text ~= LastChatSpam) then
                   ChatFrame1:AddMessage(zoneColor..text);
                   LastChatSpam = text;
            end
        end
    elseif (ChatSpam ~= 0) then
         if (text ~= LastChatSpam) then
             --// Testing
             --// ChatFrame1:AddMessage("Text is: \""..text.."\"");
             if (zoneColor ~= "") then
                ChatFrame1:AddMessage(zoneColor..text);
                   LastChatSpam = text;
               elseif (text ~= "") then
                   ChatFrame1:AddMessage(text);
                   LastChatSpam = text;
               end
        end
    end
end

function EZoneLevel_SetTooltip()
    level = UnitLevel("player");
    zone=GetSubZoneText();
     if ((zone == "") or (EZLL[zone] == -6)) then 
       zone=GetZoneText();
    end
    ZL_low = EZLL[zone];
    if ( ZL_low == nil ) then
        zone=GetZoneText();
        ZL_low = EZLL[zone];
    else
          ZL_high = EZLH[zone];
          local mid = (ZL_low + ZL_high)/2;
        if(ZL_high < level-5) then -- Grey icon
            zoneColor = EZLGrey;
        elseif(ZL_low > level) then -- Red icon
            zoneColor = EZLRed;
        elseif(mid < level) then -- Green icon
            zoneColor = EZLGreen;
        else -- Yellow icon
            zoneColor = EZLYellow;
        end
       end
    if (zoneColor ~= "") then
        text = zoneColor;
    else
        text = EZLWhite;
    end
    text = text..zone.."|r\n";
    if (ZL_low ~= nil) then
        text = text.."Low  : "..ZL_low;
    end
   if (ZL_low ~= nil) then
        text = text.."\nHigh : "..ZL_high;
    end
    GameTooltip:SetText(text);
end


